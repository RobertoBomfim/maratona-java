package academy.devdojo.maratonajava.introducao.variaveis;

public class Aula02TiposPrimitivos {
    public static void main(String[] args) {
        //variáveis Java (tipos primitivos): int, double, float, char, byte, short, long, boolean
        int valor = 29;
        double altura = 1.77;
        float peso = 61.50F;
        byte idadeByte = 10;
        short idadeShort = 10;
        boolean verdadeiro = true;
        boolean falso = false;
        char primeiraLetra = 'R';

        //Não primitivo
        String nome = "Roberto";
        System.out.println("O valor é " + valor + " reais");
        System.out.println("Oi, meu nome é " + nome);

    }
}
